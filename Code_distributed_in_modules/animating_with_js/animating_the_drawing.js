function draw() {
  background("#1F2041");
  translate(940, 486);

  const scaleCoeff = 3;

  let intiX = 0;
  let initY = 0;

  let x = 0;
  let y = 0;

  for (let k = 0; k < fourierCoefficients.length; k++) {
    let frequency = freqArray[k];

    intiX = x;
    initY = y;

    // stroke(255, 50);
    // noFill();
    // ellipse(intiX, initY, radius * 2);

    x +=
      scaleCoeff *
      (math.re(math.complex(fourierCoefficients[k])) *
        cos(2 * Math.PI * frequency * time) +
        math.im(math.complex(fourierCoefficients[k])) *
          sin(2 * Math.PI * frequency * time));
    y +=
      scaleCoeff *
      (-1 *
        math.im(math.complex(fourierCoefficients[k])) *
        cos(2 * Math.PI * frequency * time) +
        math.re(math.complex(fourierCoefficients[k])) *
          sin(2 * Math.PI * frequency * time));

    // x += radius * cos(2 * Math.PI * frequency * time + phase);
    // y += radius * sin(2 * Math.PI * frequency * time + phase);

    fill("#E9D2C0");
    stroke("#E9D2C0");
    line(x, y, intiX, initY);
    ellipse(x, y, 5);
  }

  drawx.unshift(x);
  drawy.unshift(y);

  beginShape();
  noFill();
  for (let i = 0; i < drawx.length; i++) {
    vertex(drawx[i], drawy[i]);
  }
  endShape();

  point_limit = 1000;

  if (drawx.length > point_limit) {
    drawx.pop();
  }
  if (drawy.length > point_limit) {
    drawy.pop();
  }

  time += 0.001;
}
